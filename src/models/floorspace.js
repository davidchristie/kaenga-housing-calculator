export function getBathtubFloorspace (bathtubs) {
  return bathtubs * 7.8
}

export function getDoubleBedroomFloorspace (bedrooms) {
  return bedrooms * 12.25
}

export function getShowerFloorspace (showers) {
  return showers * 5.2
}

export function getSingleBedroomFloorspace (bedrooms) {
  return bedrooms * 8.5
}

export function getStudyFloorspace (study) {
  return study * 6.72
}

export function getToiletFloorspace (toilets) {
  return toilets * 1.4
}
